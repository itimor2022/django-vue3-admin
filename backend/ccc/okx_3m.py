def calculate_fractals(df):
    df['m_high'] = df['high'].rolling(30).max()
    df['m_low'] = df['low'].rolling(30).min()
    """Calculate fractals"""
    df['f_high'] = np.where(
        (df['m_high'] == df['high']) &
        (df['high'] > df['high'].shift(1)) &
        (df['high'] >= df['high'].shift(-1)) &
        (df['high'] > df['high'].shift(2)) &
        (df['high'] >= df['high'].shift(-2))
        , np.True_, np.False_
    )

    df['f_low'] = np.where(
        (df['m_low'] == df['low']) &
        (df['low'] < df['low'].shift(1)) &
        (df['low'] <= df['low'].shift(-1)) &
        (df['low'] < df['low'].shift(2)) &
        (df['low'] <= df['low'].shift(-2))
        , np.True_, np.False_
    )
    return df


